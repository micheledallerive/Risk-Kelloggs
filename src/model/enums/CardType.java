package model.enums;

/**
 * Describes the figure represented by a card.
 *
 * @author dallem @usi.ch
 */
public enum CardType {
    /**
     * Infantry figure.
     */
    INFANTRY,
    /**
     * Cavalry figure.
     */
    CAVALRY,
    /**
     * Artillery figure.
     */
    ARTILLERY,
    /**
     * The card is a wild card, representing all the figures.
     */
    WILD
}
