package model.enums;

/**
 * The color of the die.
 *
 * @author moralj @usi.ch
 */
public enum DieColor {
    /**
     * The die is red, meaning it is used to attack.
     */
    RED,
    /**
     * The die is blue, meaning it is used to defend.
     */
    BLUE,
    /**
     * The die is white, meaning it is used as a neutral die.
     */
    WHITE
}
